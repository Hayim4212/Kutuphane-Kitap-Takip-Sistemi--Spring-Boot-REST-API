package com.hasimsolak.dto;

public class BookDTO {
	
	private String name;

	private String author;
	
	public BookDTO() {
		
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getAuthor() {
		return author;
	}

	public void setAuthor(String author) {
		this.author = author;
	}
	
}
